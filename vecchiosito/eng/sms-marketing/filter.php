				<h2>Using the Filter</h2>
				<p>
				You can choose to send <strong>multiple SMS</strong> to some of your customers, using the powerfull filter of Gestione Messaggi; in that case, a window will open, where you will can choose to which the customers you want to send the SMS, dependign their interests, or their average spending, and much other. To do this, simply choose you the YES option from the first menu on the top of the "Send SMS" window, "Want you use a filter...." then finish the sending of SMS.
				</p>
				<p>
				The procedure to use the "Gestione Messaggi Filter" it is divided in two steps: 
				</p>
				<p>
				1. In the first step, called "1 of 2", you can configure the Filter, which lets you to filter the SMS receivers depending their name or surname, or their type (Individual or Company), their Gender , or also depending their Mobile Operator, or their Age (with the possibility to send theSMS to "which the Birthday is", or  finally the "Performance" you did (Buy, Sell, Reso).</p>
				<p>
				2. In the "2 / 2" step you can filter the customer's place of residence, as the items are selected from the lists, they also appear the related geographic subareas. 
				</p>
				<h2>Sending <strong>multiple SMS</strong> without the Filter</h2>
				<p>
				It is also possible to choose a global SMS send, just checking the NO option for the "Want you a filter...." in the first menu, at upper side of the "Send Message" window, and so proceeding to the send of SMS. 
				</p>
