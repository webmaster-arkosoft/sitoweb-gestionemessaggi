				<h1><a rel="bookmark" title="Permalink to Inserting customer management software, sms marketing messages to multiple sms" href="customerinput.html#descrizioneguida"> Inserting Customers </a></h1>
				<p>Now we can insert the customers  in order to sort <strong>sms multipli</strong> filtering them for customers interests or other characteristics.</p>
				<p>The insert of new customers can be done in any moment, you have just to go to the main user interface and press the "New Customers" button, then follow the procedure:</p>
				<p>1. In the first interface, choose the type of client, using the "Subscribe the following User" menu: here is possible to choose an or a Company; <b>NOTE:</b>
				clicking on any of the following fields, then it starts the data insert.</p>
				<p>
					<img src="img/nuovocliente1.jpg" alt="Inserting Customers" class="centra"/>
				</p>
				<p>2. In the following window, insert all the customer's anagraphic data: in our case, we insert an Individual with its data like "Birthday", or its "City", plus the "Name" and the "Surname";</p>
				<p>
					<img src="img/nuovocliente2.jpg" alt="Inserting Customers" class="centra"/>
				</p>
				<p>3.  Once all data are inserted, like the picture shows, click on "Next" in order to open the next window;</p>
				<p>4. It will appear another window, where we will insert all the following customer contacts: "Mobile Number" and "Operator" are necessaries, while other ones like "Home Telephone", "Fiscal Code" or "E-mail", they are facultatives;</p>
				<p>5. Once the Customers are completes, we can end the procedure with the "End" button, while with the following left button, we return to the previous window (Indfvidual 1 and 2)</p>
				<p>
					<img src="img/nuovocliente3.jpg" alt="Inserting Customers" class="centra"/>
				</p>
				<p><b>NOTE:</b> when a Company Customer type is created, is also possible to assign to it a legal representant, simply checking the "Subscribe a legal representant" checkbox, in the bottom of the window. It will open another window, the same as that used to create an Individual Customer.</p>
				<p>
					<img src="img/nuovocliente4.jpg" alt="Inserting Customers" class="centra"/>
				</p>
				<p>6. In order to complete the customer inserting, you have to assign it some interests, which we will explain in the next chapter of this User Guide.</p>
